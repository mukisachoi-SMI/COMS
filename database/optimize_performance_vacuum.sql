-- ⚠️ 주의사항:
-- 1. 이 명령어들은 각각 별도로 실행해야 합니다
-- 2. 트랜잭션 블록 내에서 실행할 수 없습니다
-- 3. 각 명령어를 하나씩 복사해서 실행하세요

-- 테이블 공간 정리 및 통계 업데이트
VACUUM ANALYZE donations;

-- 다음 명령어를 별도로 실행
VACUUM ANALYZE members;

-- 다음 명령어를 별도로 실행
VACUUM ANALYZE donation_types;

-- 다음 명령어를 별도로 실행
VACUUM ANALYZE churches;

-- ============================================
-- 💡 팁:
-- - VACUUM은 삭제된 행의 공간을 재사용 가능하게 만듭니다
-- - ANALYZE는 쿼리 플래너의 통계를 업데이트합니다
-- - 정기적으로 (월 1회) 실행하는 것이 좋습니다
